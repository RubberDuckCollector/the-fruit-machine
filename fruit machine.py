def game():
    
    global credit
    global ended
    
    roll1 = randint(0, len(item)-1)
    roll2 = randint(0, len(item)-1)
    roll3 = randint(0, len(item)-1)

    def choice1():
        if roll1 == 0:
            return f"{color.Red}{roll_list[roll1]}{color.Reset}"
        elif roll1 == 1:
            return f"{color.Cyan}{roll_list[roll1]}{color.Reset}"
